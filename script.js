/* ============ Mini hra ============ */
(function(){
  const canvas = document.getElementById("gameCanvas");
  const ctx = canvas.getContext("2d");
  const W = canvas.width, H = canvas.height;
  const hudScore = document.getElementById("hudScore");
  const hudLives = document.getElementById("hudLives");
  const startOv = document.getElementById("gameStart");
  const overOv = document.getElementById("gameOver");
  const finalScore = document.getElementById("finalScore");

  const excavator = { x: W/2, y: H-70, w: 130, h: 60, speed: 7 };
  let blocks = [];     // {x,y,w,h,vy,type:'brick'|'beam'}
  let score = 0, lives = 3, running = false, paused = false, spawnTimer = 0, difficulty = 1;
  const keys = {};

  function reset(){
    blocks = []; score = 0; lives = 3; difficulty = 1; spawnTimer = 0;
    excavator.x = W/2;
    updateHud();
  }
  function updateHud(){
    hudScore.textContent = score;
    hudLives.textContent = lives > 0 ? "❤".repeat(lives) : "—";
  }

  function spawn(){
    const w = 28 + Math.random()*22;
    const isBeam = Math.random() < 0.25;
    blocks.push({
      x: Math.random()*(W-w),
      y: -30,
      w: isBeam ? w*2.5 : w,
      h: isBeam ? 12 : w*0.7,
      vy: 2 + Math.random()*2 + difficulty*0.6,
      type: isBeam ? "beam" : "brick"
    });
  }

  function drawBg(){
    // sky gradient
    const g = ctx.createLinearGradient(0,0,0,H);
    g.addColorStop(0,"#3a2a1a"); g.addColorStop(1,"#0d0c0b");
    ctx.fillStyle = g; ctx.fillRect(0,0,W,H);
    // sun
    ctx.fillStyle = "rgba(245,165,36,.35)";
    ctx.beginPath(); ctx.arc(W*0.78, H*0.35, 90, 0, Math.PI*2); ctx.fill();
    // ground
    ctx.fillStyle = "#1a1714"; ctx.fillRect(0, H-30, W, 30);
    // rubble
    ctx.fillStyle = "#2d2520";
    for(let i=0;i<20;i++){
      const rx = (i*47 + 13)%W;
      ctx.fillRect(rx, H-30-5, 8, 6);
    }
  }

  function drawExcavator(e){
    // tracks
    ctx.fillStyle = "#1a1714";
    ctx.fillRect(e.x - e.w/2, e.y + 30, e.w, 18);
    ctx.fillStyle = "#0d0c0b";
    for(let i=0;i<6;i++) ctx.fillRect(e.x - e.w/2 + 4 + i*20, e.y+34, 12, 10);
    // body (yellow)
    ctx.fillStyle = "#f5a524";
    ctx.fillRect(e.x - e.w/2 + 10, e.y, e.w - 20, 32);
    // cab
    ctx.fillStyle = "#15110d";
    ctx.fillRect(e.x - e.w/2 + 18, e.y - 22, 38, 24);
    ctx.fillStyle = "#e8e4dd";
    ctx.fillRect(e.x - e.w/2 + 22, e.y - 18, 30, 16);
    // arm
    ctx.strokeStyle = "#f5a524"; ctx.lineWidth = 8;
    ctx.beginPath();
    ctx.moveTo(e.x + 20, e.y);
    ctx.lineTo(e.x + 55, e.y - 30);
    ctx.lineTo(e.x + 70, e.y);
    ctx.stroke();
    // bucket (catcher area)
    ctx.fillStyle = "#15110d";
    ctx.fillRect(e.x + 55, e.y - 4, 30, 14);
    ctx.fillStyle = "#f5a524";
    ctx.fillRect(e.x + 55, e.y - 4, 30, 4);
  }

  function drawBlock(b){
    if(b.type === "beam"){
      ctx.fillStyle = "#5a5550";
      ctx.fillRect(b.x, b.y, b.w, b.h);
      ctx.fillStyle = "#3a3530";
      ctx.fillRect(b.x, b.y+b.h-3, b.w, 3);
    } else {
      ctx.fillStyle = "#a8523a";
      ctx.fillRect(b.x, b.y, b.w, b.h);
      ctx.strokeStyle = "#6b3422"; ctx.lineWidth = 1;
      ctx.strokeRect(b.x, b.y, b.w, b.h);
      ctx.beginPath();
      ctx.moveTo(b.x, b.y+b.h/2); ctx.lineTo(b.x+b.w, b.y+b.h/2);
      ctx.stroke();
    }
  }

  function catcherRect(){
    return { x: excavator.x + 55, y: excavator.y - 6, w: 30, h: 8 };
  }

  function step(){
    if(!running){ requestAnimationFrame(step); return; }
    if(paused){
      drawPaused();
      requestAnimationFrame(step); return;
    }

    // input
    if(keys["ArrowLeft"] || keys["a"]) excavator.x -= excavator.speed;
    if(keys["ArrowRight"]|| keys["d"]) excavator.x += excavator.speed;
    excavator.x = Math.max(excavator.w/2, Math.min(W - excavator.w/2 - 20, excavator.x));

    // spawn
    spawnTimer++;
    const spawnEvery = Math.max(28, 70 - difficulty*4);
    if(spawnTimer > spawnEvery){ spawn(); spawnTimer = 0; }

    // update
    const cr = catcherRect();
    for(const b of blocks){
      b.y += b.vy;
      // catch?
      if(b.y + b.h >= cr.y && b.y <= cr.y + cr.h &&
         b.x + b.w >= cr.x && b.x <= cr.x + cr.w){
        if(b.type === "beam"){ lives--; b.dead = true; flash = 12; }
        else { score++; b.dead = true; if(score % 10 === 0) difficulty++; }
        updateHud();
      }
      // arm hit
      const armY = excavator.y - 30;
      if(b.type === "beam" && b.y + b.h >= armY && b.y + b.h <= armY+12 &&
         b.x + b.w >= excavator.x + 20 && b.x <= excavator.x + 70){
        lives--; b.dead = true; flash = 12; updateHud();
      }
      if(b.y > H) b.dead = true;
    }
    blocks = blocks.filter(b => !b.dead);

    if(lives <= 0){ endGame(); }

    // draw
    drawBg();
    blocks.forEach(drawBlock);
    drawExcavator(excavator);

    if(flash > 0){
      ctx.fillStyle = `rgba(245,80,40,${flash/20})`;
      ctx.fillRect(0,0,W,H);
      flash--;
    }

    requestAnimationFrame(step);
  }

  function drawPaused(){
    drawBg();
    blocks.forEach(drawBlock);
    drawExcavator(excavator);
    ctx.fillStyle = "rgba(13,12,11,.7)"; ctx.fillRect(0,0,W,H);
    ctx.fillStyle = "#f5a524"; ctx.font = "bold 42px Archivo Black, system-ui";
    ctx.textAlign = "center"; ctx.fillText("PAUZA", W/2, H/2);
  }

  let flash = 0;

  function start(){
    reset();
    running = true; paused = false;
    startOv.classList.add("hidden"); overOv.classList.add("hidden");
  }
  function endGame(){
    running = false;
    finalScore.textContent = score;
    overOv.classList.remove("hidden");
  }

  document.getElementById("btnStart").addEventListener("click", start);
  document.getElementById("btnRestart").addEventListener("click", start);

  window.addEventListener("keydown", e=>{
    keys[e.key] = true;
    if(e.key === " " && running){ paused = !paused; e.preventDefault(); }
    if(["ArrowLeft","ArrowRight"," "].includes(e.key)) e.preventDefault();
  });
  window.addEventListener("keyup", e=>{ keys[e.key] = false; });

  // touch
  let touchX = null;
  canvas.addEventListener("touchstart", e=>{ touchX = e.touches[0].clientX; }, {passive:true});
  canvas.addEventListener("touchmove", e=>{
    if(touchX===null) return;
    const rect = canvas.getBoundingClientRect();
    const x = (e.touches[0].clientX - rect.left) * (W/rect.width);
    excavator.x = Math.max(excavator.w/2, Math.min(W - excavator.w/2 - 20, x));
  }, {passive:true});

  // initial draw
  drawBg(); drawExcavator(excavator);
  requestAnimationFrame(step);
})();
